// ── Main ──────────────────────────────────────────────────────────────────────
async function seed() {
  // Resolve contributor ID from existing admin account
  const { data: contributors, error: fetchError } = await supabase
    .from('contributors')
    .select('id, email')
    .order('created_at', { ascending: true })
    .limit(1)

  if (fetchError || !contributors?.length) {
    console.error('No contributors found. Sign in to the app first to create your account, then re-run this script.')
    process.exit(1)
  }

  const contributorId = contributors[0].id
  console.log(`Seeding as contributor: ${contributors[0].email} (${contributorId})`)
  console.log(`Seeding ${requirements.length} requirements...`)

  const rows = requirements.map(r => ({
    contributor_id: contributorId,
    raw_input: r.raw_input,
    refined_title: r.refined_title,
    user_story: r.user_story,
    refined_description: r.refined_description,
    acceptance_criteria: r.acceptance_criteria,
    persona_type: r.persona_type,
    category: r.category,
    priority_suggestion: r.priority_suggestion,
    tags: r.tags,
    status: r.status,
    upvotes: Math.floor(Math.random() * 40) + 5,
  }))

  const { data, error } = await supabase
    .from('requirements')
    .insert(rows)
    .select('id, refined_title')

  if (error) {
    console.error('Failed to insert requirements:', error.message)
    process.exit(1)
  }

  console.log(`\n✓ Seeded ${data.length} requirements:\n`)
  data.forEach(r => console.log(`  • ${r.refined_title}`))
  console.log()
}

seed()
